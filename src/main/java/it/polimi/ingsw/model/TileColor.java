package it.polimi.ingsw.model;


/**
 * This class contains all the possible color value for the tiles of the game; color "Empty" represents a position
 * of the board which is empty, but it can contain a card; color "Invalid" represents a position which is not part
 * of the board
 */
public enum TileColor {
    /**
     * color green
     */
    GREEN,
    /**
     * color white
     */
    WHITE,

    /**
     * color blue
     */
    BLUE,
    /**
     * color yellow
     */
    YELLOW,
    /**
     * color cyan
     */
    CYAN,
    /**
     * color violet
     */
    VIOLET,
    /**
     * color empty
     */
    EMPTY,
    /**
     * color invalid
     */
    INVALID

}
